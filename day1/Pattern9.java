public class Pattern9 {
    public static void main(String[] args) {
        // Define the pattern
        String[] pattern = {
                "    *",
                "   ***",
                "  *****",
                "   ***",
                "    *"
        };

        // Print the pattern
        for (String line : pattern) {
            System.out.println(line);
        }
    }
}

